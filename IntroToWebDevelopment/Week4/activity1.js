$(document).ready(function() {

    $(".btn-default").click(function() {
        $("h1").css("color", "black");
    });

    $(".btn-primary").click(function() {
        $("h1").css("color", "blue");
    });

    $(".btn-success").click(function() {
        $("h1").css("color", "green");
    });

    $(".btn-info").click(function() {
        $("h1").css("color", "turquoise");
    });

    $(".btn-warning").click(function() {
        $("h1").css("color", "orange");
    });

    $(".btn-danger").click(function() {
        $("h1").css("color", "red");
    });

});
